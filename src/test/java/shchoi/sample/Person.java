package shchoi.sample;

public class Person {
    private Long id;
    private String name;
    private String address;
    private String email;

    public Person() {

    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getAddress() {
        return address;
    }

    public String getEmail() {
        return email;
    }
}
